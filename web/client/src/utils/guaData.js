/**
 * 根据六爻数组生成卦码
 * @param {array} yaos - 六爻数组，每个元素包含 value (0或1) 和 isMoving
 * @returns {string} 六位二进制卦码
 */
export function generateGuaCode(yaos) {
  let code = '';
  for (let i = 0; i < 6; i++) {
    code += yaos[i].value.toString();
  }
  return code;
}

/**
 * 根据六爻数组生成变卦卦码
 * @param {array} yaos - 六爻数组
 * @returns {string} 变卦的六位二进制卦码
 */
export function generateBianGuaCode(yaos) {
  let code = '';
  for (let i = 0; i < 6; i++) {
    if (yaos[i].isMoving) {
      // 动爻变化：阳变阴，阴变阳
      code += yaos[i].value === 1 ? '0' : '1';
    } else {
      code += yaos[i].value.toString();
    }
  }
  return code;
}

/**
 * 获取动爻位置数组
 * @param {array} yaos - 六爻数组
 * @returns {array} 动爻位置数组（1-6）
 */
export function getMovingYaoPositions(yaos) {
  const positions = [];
  for (let i = 0; i < yaos.length; i++) {
    if (yaos[i].isMoving) {
      positions.push(i + 1);
    }
  }
  return positions;
}

/**
 * 获取农历日期
 * @returns {string} 农历日期字符串
 */
export function getLunarDate() {
  const now = new Date();
  return `${now.getFullYear()}年${now.getMonth() + 1}月${now.getDate()}日`;
}
